console.log("--- Quadrados na Faixa de 15 a 200 ---");

// 1. Inicialização e 2. Laço de Iteração
// Usamos o laço 'for' para automatizar a repetição
// (início; condição de parada; incremento)
for (let i = 15; i <= 200; i++) {
    
    // Calculamos o quadrado do número atual da iteração
    let quadrado = i ** 2;
    
    // 3. Saída de Dados
    // Exibimos o resultado formatado
    console.log(`O quadrado de ${i} é: ${quadrado}`);
}

console.log("--- Fim do Processamento ---"); 